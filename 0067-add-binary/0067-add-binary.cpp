class Solution {
public:
    string addBinary(string a, string b) {
        string rett;
        int carry=0;
        int i=a.size()-1,j=b.size()-1;
        while(i>=0 ||j>=0 || carry!=0){
            int sum=carry;
            if(i>=0){
                sum+=a[i]-'0';
            }
            if(j>=0){
                sum+=b[j]-'0';
            }
            rett.push_back(sum%2+'0');
            carry=sum/2;
            i--;
            j--;
        }
        reverse(rett.begin(), rett.end());
        return rett;
    }
};